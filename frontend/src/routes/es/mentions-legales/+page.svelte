    <h3>Datos recopilados</h3>
    <p>
      CleanPoker únicamente recopila el <strong>seudónimo</strong> que introduce libremente
      para unirse a una sesión. Este seudónimo se muestra a los demás participantes de la misma sesión.
      No se solicita ni almacena ninguna dirección de correo electrónico, número de teléfono
      ni dato de identificación real.
    </p>

    <h3>Finalidad y base legal</h3>
    <p>
      El tratamiento tiene como única finalidad la prestación del servicio de planning poker colaborativo
      (base legal: ejecución del servicio solicitado por el usuario, art. 6.1.b RGPD).
    </p>

    <h3>Duración de conservación</h3>
    <p>
      Las sesiones y los seudónimos asociados se eliminan automáticamente
      <strong>24 horas después de la última actividad</strong>. No se conserva ningún archivo más allá de este plazo.
    </p>

    <h3>Transferencia de datos</h3>
    <p>
      Los datos del backend (seudónimos, actividad de votación) están alojados
      <strong>exclusivamente en Francia (París)</strong> en Fly.io.
