    <h3>Dados recolhidos</h3>
    <p>
      O CleanPoker recolhe apenas o <strong>pseudónimo</strong> que introduz livremente
      para entrar numa sessão. Este pseudónimo é apresentado aos outros participantes da mesma sessão.
      Não é solicitado nem armazenado qualquer endereço de e-mail, número de telefone
      ou dado de identificação real.
    </p>

    <h3>Finalidade e base legal</h3>
    <p>
      O tratamento tem como única finalidade a prestação do serviço de planning poker colaborativo
      (base legal: execução do serviço solicitado pelo utilizador, art. 6.1.b RGPD).
    </p>

    <h3>Duração de conservação</h3>
    <p>
      As sessões e os pseudónimos associados são automaticamente eliminados
      <strong>24 horas após a última atividade</strong>. Não é conservado nenhum arquivo para além deste prazo.
    </p>

    <h3>Transferência de dados</h3>
    <p>
      Os dados do backend (pseudónimos, atividade de votação) são alojados
